/**********************************************************************/
/* OBJECT ATTRIBUTES FOR THIS OBJECT (C MODULE)                       */
/**********************************************************************/
#define NENTS 4

static int optab[][NENTS] = {
   {'+', integer, integer, integer},
   {'+', real,    real,    real},
   {'+', integer, real,    real},
   {'+', real,    integer, real},
   {'*', integer, integer, integer},
   {'*', real,    real,    real},
   {'*', integer, real,    real},
   {'*', real,    integer, real},
   {'$', undef,   undef,   undef}
   };

/**********************************************************************/
/*  PRIVATE METHODS for this OBJECT  (using "static" in C)            */
/**********************************************************************/

/**********************************************************************/
/*  PUBLIC METHODS for this OBJECT  (EXPORTED)                        */
/**********************************************************************/
/**********************************************************************/
/* display the op tab                                                 */
/**********************************************************************/
static void p_optabrow(int row){
    printf("%10s%10s%10s%10s\n", tok2lex(optab[row][0]), tok2lex(optab[row][1]),
                                 tok2lex(optab[row][2]), tok2lex(optab[row][3]));
}

void p_optab()
{
    puts("\n________________________________________________________ ");
    puts(" THE OPERATOR TABLE");
    puts("________________________________________________________ ");
    puts("   operator       arg1       arg2     result");
    puts("________________________________________________________ ");
    for(int i = 0; optab[i][0] != '$'; i++){p_optabrow(i);}
    puts("________________________________________________________ ");
}

/**********************************************************************/
/* return the type of a binary expression op arg1 arg2                */
/**********************************************************************/
int get_otype(int op, int arg1, int arg2)
{
    for (int i = 0; optab[i][0] != '$'; i++) {
        if (optab[i][0] == op && optab[i][1] == arg1 && optab[i][2] == arg2)
            return optab[i][3];
    }
    return undef;
}

